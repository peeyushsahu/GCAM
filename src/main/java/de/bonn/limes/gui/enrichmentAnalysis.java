    private void analyseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_analyseActionPerformed
        String test = "fisher";
        int synonym = 0;
        float Ethreshold;
        float Pthreshold;
        
        if(binomTest.isSelected()){
            test = "binomial";
        }
        if(yesButton.isSelected()){
            synonym = 1;
        }
        
        Ethreshold = Float.parseFloat(enrichmentThreshold.getText());
        if(Ethreshold==0){
            Ethreshold = (float) 0.2;
        }
        Pthreshold = Float.parseFloat(pvalueThreshold.getText());
        if(Pthreshold==0){
            Pthreshold = (float) 0.001;
        }
            
          try {
            SourcingRFile DoinR = new SourcingRFile();
            DoinR.checkLocalRserve();
            DoinR.analyse(Pthreshold,Ethreshold,synonym,test);
            this.dispose();
            
        } catch (RserveException ex) {
            Logger.getLogger(GeneMinerUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (REXPMismatchException | REngineException ex) {
            ex.printStackTrace();
        } catch (InterruptedException ex) {
            Logger.getLogger(enrichmentAnalysis.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_analyseActionPerformed

